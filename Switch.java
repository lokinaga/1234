package day6;

public class Switch {
	public static void main (String[]args) {
		int num = 5;
		switch(num) {
		case 1:
		System.out.println("north");
		break;
		case 2:
			System.out.println("east");
		break;
		case 3:
			System.out.println("south");
		 break;
		 case 4:
			 System.out.println("west");
		 default:
		 System.out.println("invalid");
		
		
		}
		
		
		
		
		
		
		

}
}